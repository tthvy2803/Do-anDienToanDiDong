    private void btnGuiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuiActionPerformed
                                      
    String noiDung = txtNhapTin.getText().trim();
    String nguoiNhan = listBanBe.getSelectedValue();

    if (nguoiNhan == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn một người để nhắn tin!");
        return;
    }
    if (noiDung.isEmpty()) return;

    try (java.sql.Connection cn = DBConnect.getConnection()) {
        String sql = "INSERT INTO TinNhan (NguoiGui, NguoiNhan, NoiDung) VALUES (?, ?, ?)";
        java.sql.PreparedStatement pst = cn.prepareStatement(sql);
        
        pst.setString(1, LoginForm.currentUser); // Lấy từ biến static ở LoginForm
        pst.setString(2, nguoiNhan); 
        pst.setString(3, noiDung);
        
        pst.executeUpdate();
        txtNhapTin.setText(""); 
        loadTinNhan(); // Cập nhật ngay lập tức khung chat
    } catch (Exception e) {
        e.printStackTrace();
    }

    }//GEN-LAST:event_btnGuiActionPerformed

    private void txtTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtTimKiemActionPerformed
                                         
    String search = txtTimKiem.getText().trim();
    DefaultListModel<String> model = new DefaultListModel<>();
    try (java.sql.Connection cn = DBConnect.getConnection()) {
        String sql = "SELECT TenDangNhap FROM TaiKhoan WHERE TenDangNhap LIKE ? AND LoaiTaiKhoan = 0 AND TenDangNhap != ?";
        java.sql.PreparedStatement pst = cn.prepareStatement(sql);
        pst.setString(1, "%" + search + "%");
        pst.setString(2, LoginForm.currentUser);
        java.sql.ResultSet rs = pst.executeQuery();
        while (rs.next()) {
            model.addElement(rs.getString("TenDangNhap"));
        }
        listBanBe.setModel(model);
    } catch (Exception e) { e.printStackTrace(); }

    }//GEN-LAST:event_txtTimKiemActionPerformed


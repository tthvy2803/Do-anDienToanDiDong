public void loadTinNhan() {
    String nguoiNhan = listBanBe.getSelectedValue();
    if (nguoiNhan == null) return;

    try (java.sql.Connection cn = DBConnect.getConnection()) {
        String sql = "SELECT NguoiGui, NoiDung FROM TinNhan WHERE "
                   + "(NguoiGui = ? AND NguoiNhan = ?) OR (NguoiGui = ? AND NguoiNhan = ?) "
                   + "ORDER BY ThoiGian ASC";
        
        java.sql.PreparedStatement pst = cn.prepareStatement(sql);
        pst.setString(1, LoginForm.currentUser);
        pst.setString(2, nguoiNhan);
        pst.setString(3, nguoiNhan);
        pst.setString(4, LoginForm.currentUser);
        
        java.sql.ResultSet rs = pst.executeQuery();
        StringBuilder sb = new StringBuilder();
        while (rs.next()) {
            String gui = rs.getString("NguoiGui");
            String nd = rs.getString("NoiDung");
            // Đánh dấu "Bạn: " cho dễ nhìn
            if (gui.equals(LoginForm.currentUser)) sb.append("Bạn: ").append(nd).append("\n");
            else sb.append(gui).append(": ").append(nd).append("\n");
        }
        areaNoiDungChat.setText(sb.toString());
    } catch (Exception e) {
        // Tránh in lỗi liên tục khi Timer chạy
    }
}
